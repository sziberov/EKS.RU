		<b>Название:</b> DAEMON Tools Lite<br>
		<b>Версия:</b> 5.0.1.0407<br>
		<b>Лицензия:</b> freeware<br>
		<b>Интерфейс:</b> Многоязычный (English, Русский, Українська)<br>
