	</p>
	<p>
		<b>Описание:</b><br>
		Daemon Tools - маленький по размеру, но мощный по возможностям эмулятор CD/DVD-драйвов - позволяет "подцепить" любой образ диска, сделанный какой-нибудь утилитой для копирования дисков (например, CloneCD), т.е. может работать с какими угодно "дисками" - даже с PlayStation. Точно также в этот эмулятор можно "вставить" скопированные на винчестер аудио-CD или DVD-диски.
	</p>
	<p>
		Программа работает через меню в системном трее, при помощи которого вы получаете доступ ко всем командам. Тут можно выбрать количество добавляемых виртуальных приводов, образы для монтирования на каждый из них. Для обозначения каждого привода можно выбрать любую букву английского алфавита, которая еще не используется. Также для каждого виртуального устройства может быть установлен свой региональный код. Стоит отметить, что подключение и отключение виртуальных приводов происходит почти мгновенно и без необходимости перезагрузки. Возможности программы можно расширить при помощи дополнительных модулей, которые в большом количестве выпускаются сторонними разработчиками.
	</p>
	<p>
		<b>Системные требования:</b><br>
		Поддерживает все операционные системы Windows начиная от XP до 10.
	</p>
	<p>
		<b>Основные возможности:</b><br>
		• Виртуализация приводов.<br>
		• Создание до 4-х виртуальных приводов (2 DT и 2 SCSI) для монтирования нескольких образов.<br>
	</p>
	<p>
		<b>Настройка виртуальных дисков:</b><br>
		• Изменение буквы диска, DVD региона и т.д.<br>
		• Монтирование *.mdx, *.mds/*.mdf, *.iso, *.b5t, *.b6t, *.bwt, *.ccd, *.cdi, *.bin/*.cue, *.ape/*.cue, *.nrg, *.pdi, *.isz образов на виртуальные приводы.<br>
	</p>
	<p>
		<b>Основные функции резервного копирования:</b><br>
		• Создание .iso, *.mds/*.mdf and *.mdx образов из CD/DVD/Blu-ray дисков.<br>
		• Защита образов паролем.<br>
		• Сжатие образов.<br>
		• Управление Каталогом образов.<br>
	</p>
	<p>
		<u><font color="#13AE1B"><b>Особенности версии:</b></font></u><br>
